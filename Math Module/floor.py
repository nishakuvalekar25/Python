# In Python, the floor function is part of the math module, and it is used to round a number down to the nearest integer.

import math

# Example 1
number1 = 5.8
floored_number1 = math.floor(number1)
print("The floor of", number1, "is:", floored_number1)

# Example 2
number2 = -3.2
floored_number2 = math.floor(number2)
print("The floor of", number2, "is:", floored_number2)


# Output

# The floor of 5.8 is: 5
# The floor of -3.2 is: -4