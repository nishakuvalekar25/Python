# In Python, the ceil function is part of the math module, and it is used to round a number up to the nearest integer.

import math

# Example 1
number1 = 5.3
ceiled_number1 = math.ceil(number1)
print("The ceiling of", number1, "is:", ceiled_number1)

# Example 2
number2 = -2.7
ceiled_number2 = math.ceil(number2)
print("The ceiling of", number2, "is:", ceiled_number2) 



# Output

# The ceiling of 5.3 is: 6
# The ceiling of -2.7 is: -2