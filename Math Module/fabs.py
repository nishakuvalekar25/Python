# In Python, the fabs function is part of the math module, and it is used to get the absolute value of a floating-point number.

import math

# Example 1
number1 = -7.8
absolute_value1 = math.fabs(number1)
print("The absolute value of", number1, "is:", absolute_value1)

# Example 2
number2 = 3.14
absolute_value2 = math.fabs(number2)
print("The absolute value of", number2, "is:", absolute_value2)



# Output

# The absolute value of -7.8 is: 7.8
# The absolute value of 3.14 is: 3.14